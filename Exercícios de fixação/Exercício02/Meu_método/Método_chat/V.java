public class V {
    private String mod;
    private String pl;
    private int cap;

    public V(String mod, String pl, int cap) {
        this.mod = mod;
        this.pl = pl;
        this.cap = cap;
    }

    public String getPl() {
        return pl;
    }

    public String getMod() {
        return mod;
    }

    public int getCap() {
        return cap;
    }
}
