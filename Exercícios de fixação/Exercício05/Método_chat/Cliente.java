public class Cliente {
    private String n;
    private String e;
    private String mail;

    public Cliente(String n, String e, String mail) {
        this.n = n;
        this.e = e;
        this.mail = mail;
    }

    public String getN() {
        return n;
    }

    public String getE() {
        return e;
    }

    public String getMail() {
        return mail;
    }
}
