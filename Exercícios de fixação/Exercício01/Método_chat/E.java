import java.util.Date;

public class E {
    L l;
    U u;
    Date de;
    Date dd;

    public E(L l, U u, Date de, Date dd) {
        this.l = l;
        this.u = u;
        this.de = de;
        this.dd = dd;
    }

    public L getL() {
        return l;
    }

    public U getU() {
        return u;
    }

    public Date getDe() {
        return de;
    }

    public Date getDd() {
        return dd;
    }
}
