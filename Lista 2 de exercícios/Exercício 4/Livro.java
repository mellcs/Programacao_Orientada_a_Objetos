public class Livro {
    String titulo;
    String autor;
    int anoPublicacao;

    // Construtor para inicializar os atributos do livro
    public Livro(String titulo, String autor, int anoPublicacao) {
        this.titulo = titulo;
        this.autor = autor;
        this.anoPublicacao = anoPublicacao;
    }
}
